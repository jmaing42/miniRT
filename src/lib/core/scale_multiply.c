#include "minirt/core/math.h"

t_minirt_scale	minirt_scale_multiply(
	t_minirt_scale scale,
	t_minirt_float f
)
{
	return ((t_minirt_scale){
		scale.x * f,
		scale.y * f,
		scale.z * f,
	});
}
